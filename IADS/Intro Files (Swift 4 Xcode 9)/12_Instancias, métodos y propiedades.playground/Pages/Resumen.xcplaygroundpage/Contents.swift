 ## Resumen
 
 En esta lección, aprendiste que cada valor de un tipo particular se llama “instancia”.
 
 También aprendiste que los tipos no son solo una etiqueta como `String` o `Int`, sino que cada tipo puede tener funcionalidades adicionales.
 
 Los tipos pueden tener propiedades, que permiten que cada instancia tenga su propio conjunto de valores.
 
 Los tipos pueden tener métodos de instancia, que son funciones definidas como parte de un tipo.
 
 Puedes obtener información sobre los métodos y las propiedades disponibles por medio de la documentación integrada en Xcode. Se puede acceder a la documentación con facilidad directamente desde el código presionando la tecla Option y haciendo clic en el código, o bien utilizando el inspector Quick Help (Ayuda rápida). O puedes abrir el navegador de documentación en su propia ventana y buscar lo que te interese.
 
 Aprendiste que incluso un tipo que parece simple, como un tipo `String`, tiene una gran cantidad de métodos y propiedades.
 
 También aprendiste por qué es conveniente usar propiedades y métodos de instancia en lugar de funciones definidas en el nivel superior.
 
