/*:
 ## Tipos
 En Swift, cada valor tiene un _tipo_.
 
 Al igual que los tipos de objetos en la vida real, cada tipo en Swift tiene su propio conjunto de propiedades y comportamientos.

 Ya trabajaste con dos tipos de valores diferentes:
 
 - Cadenas como `"Hola"` y `"Wimbledon"`
 - Números como `101` y `-10`
 */
var string = "Soy una cadena"
let number = 12
