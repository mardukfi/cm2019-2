 ## Camiseta favorita
 
 Si te imaginaras tu camiseta favorita, probablemente no sea un plátano. No tendría sentido. Un plátano no es una camiseta.
 
 Por otra parte, si piensas en tu fruta favorita, ya sea un plátano, una manzana u otra, es casi seguro que será algún tipo de fruta.
 
 Cuando las personas nombran algo (“fruta favorita”, por ejemplo), en general, el nombre está relacionado con un tipo de objeto en particular.
 
 La idea de “fruta favorita” solo tiene sentido si está relacionada con un tipo de fruta. No tendría sentido relacionarla con “herramienta” o “astronomía”. Tu cerebro hace un buen trabajo al asegurarse de que los nombres coincidan con el tipo de objeto correcto.
 
