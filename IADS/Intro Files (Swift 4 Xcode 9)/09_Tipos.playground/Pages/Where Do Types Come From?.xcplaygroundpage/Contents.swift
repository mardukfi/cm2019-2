 ## ¿De dónde vienen los tipos? 
 
 Swift cuenta con tipos integrados que representan los componentes fundamentales de todos los programas. Practicaste bastante tiempo con el tipo `String` y el tipo `Int`, pero hay muchos más.
 
 Forman parte de la _biblioteca estándar de Swift_. Todos los lenguajes de programación tienen algo en común: el conjunto básico de funcionalidades necesarias para realizar tareas esenciales de programación.
 
 Practicaste bastante tiempo con el tipo `String` y el tipo `Int`, pero hay muchos más. A continuación, se especifican algunos de los otros tipos de la biblioteca estándar de Swift. Por ahora, no te preocupes por lo que significa cada uno, aunque tienes vía libre para imaginarlo:
 
 - `Array`
 - `Dictionary`
 - `Set`
 - `Sequence`
 - `Error`
 - `Bool`
 
 Ten en cuenta que todos los nombres de tipo comienzan con una letra en mayúscula y, si hay varias palabras, la primera letra de cada palabra también está en mayúscula. Esto es levemente diferente de las reglas para los nombres de constantes, variables y funciones, los cuales comienzan con minúscula.
 
 La diferencia es intencional. Como los nombres de los tipos comienzan con una letra mayúscula, es fácil distinguirlos de los nombres de las funciones, las variables y las constantes. Ambas usan CamelCase, pero los tipos usan _CamelCase en mayúscula_, en lugar de _CamelCase en minúscula_.
 
