/*:
 ## Declarar variables
 
 Declarar una variable es casi igual a declarar una constante. La diferencia es que, en vez de usar la palabra clave `let`, se usa la palabra clave `var`:
 */
// Declarar una constante
let placeOfBirth = "Nueva Jersey"

// Declarar una variable
var currentLocation = "Nueva Jersey"

/*:
 Después de declarar una variable, puedes asignarle un nuevo valor:
*/
// Asignar un nuevo valor a una variable
currentLocation = "California"

