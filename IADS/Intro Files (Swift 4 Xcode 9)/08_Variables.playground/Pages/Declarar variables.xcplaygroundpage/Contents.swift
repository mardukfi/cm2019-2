/*:
 En muchos sentidos, las constantes y las variables son iguales:
- Ambas utilizan las mismas reglas para los nombres.
- Ambas asocian un nombre con un valor asignado.
- Ambas tienen un tipo específico.
 
 La diferencia es la siguiente:
 - El valor de una constante *no puede* cambiarse después de su asignación inicial.
 - El valor de una variable *puede* cambiarse después de su asignación inicial.

