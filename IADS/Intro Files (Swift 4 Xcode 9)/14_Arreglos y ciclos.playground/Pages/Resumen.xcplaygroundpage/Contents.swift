 ## Resumen
 
 Puedes usar arreglos para contener listas de elementos. Los arreglos tienen dos características principales:
 
 - Los elementos de un arreglo son todos del mismo tipo.
 - Los elementos de un arreglo están en un orden específico.
 
 Gracias a estas dos características, puedes acceder a elementos desde puntos específicos del arreglo utilizando el índice, y siempre obtendrás un valor de un tipo conocido.
 
 Estos son algunos otros detalles que aprendiste sobre los arreglos:
 
 - El primer índice de un arreglo es cero, no uno.
 - Acceder a un arreglo utilizando el índice puede ser peligroso. Si el índice utilizado se encuentra fuera de los márgenes del arreglo, se producirá una falla en el programa.
 - Puedes averiguar la cantidad de elementos que hay en un arreglo con la propiedad `count` (recuento).
 - Puedes usar ciclos `for...in` para acceder de forma segura a cada elemento del arreglo en orden, sin la necesidad de saber cuántos elementos contiene el arreglo.
 - Los arreglos mutables te permiten agregar, quitar y reemplazar elementos.
 
