 ## Las API
 
 Los desarrolladores de apps siempre trabajan con código que proporcionan otras personas. En el caso de BoogieBot, hay un número de funciones definidas que hacen que BoogieBot se inicie, se mueva y cambie de color.
 
 El conjunto de funciones que controlan a BoogieBot es un ejemplo de una _interfaz de programación de aplicaciones_, más conocida como _API_.
 
 Una API es un conjunto específico de funcionalidades que puede usar un desarrollador de software para llevar a cabo una tarea. En este caso, la API de BoogieBot hace que baile un robot de software.
 
  ### API de BoogieBot
 Iniciar BoogieBot:
 - `startBot()`
 
 Mover a BoogieBot:
 
 - `leftArmUp()`, `leftArmDown()`, `rightArmUp()`, `rightArmDown()`
 - `leftLegUp()`, `leftLegDown()`, `rightLegUp()`, `rightLegDown()`
 - `shakeItLeft()`, `shakeItRight()`, `shakeItCenter()`
 - `jumpUp()`, `jumpDown()`
 
 Cambiar el color de BoogieBot:
 - `fabulize()`, `defabulize()`
 
 A continuación, aprenderás cómo juntar estas funciones para crear rutinas de baile.
 
