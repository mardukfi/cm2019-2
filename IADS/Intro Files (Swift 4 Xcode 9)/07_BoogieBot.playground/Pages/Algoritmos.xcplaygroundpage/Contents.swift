 ## Algoritmos
 
 Durante esta lección, has creado distintas coreografías para BoogieBot combinando movimientos para crear funciones. Los expertos en ciencias de la computación dirían que en el transcurso de esta lección desarrollaste _algoritmos_.
 
Cuando desarrollas un algoritmo, defines una serie de pasos que van a realizarse. Claro que no necesariamente deben ser pasos de baile; cualquier serie independiente de operaciones paso a paso es un algoritmo.
 
Definir algoritmos es una gran parte dentro del proceso de programar. Existen algoritmos matemáticos complejos que permiten hacer cosas como comprimir un video para que pueda enviarse a través de Internet de manera eficiente. Existen algoritmos que no requieren el uso de las matemáticas, como un algoritmo que permita comprobar si el usuario de una app recibió mensajes nuevos y mostrar una notificación si lo hizo. Y, por supuesto, existen algoritmos que sirven para hacer bailar a un robot de software.
 
